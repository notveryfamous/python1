print(4**4)  # “**”为乘方
print(20//3)  # “//”为取整

# round为四舍五入，默认四舍五入到整数位
a = round(8.9)
print(a)
b = round(110.3)
print(b)

aa = round(8.84, 1)  # 保留小数点1位
print(aa)
bb = round(10.3254, 2)  # 保留小数点2位
print(bb)
#语法格式为:round (小数,保留位数)

